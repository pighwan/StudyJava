package j06_Method;

public class EX01_FirstMethod {

	public static void main(String[] args) {
		System.out.println("Hello Method");
		// println : "" 안의 내용을 출력해주는 메소드를 사용
		System.out.println(Math.floor(1.1)); // floor : 내림
		// Math 안에 소속되어 있는 floor라는 메소드를 사용
		

	} // main

} // class
