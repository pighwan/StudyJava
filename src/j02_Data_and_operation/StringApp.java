package j02_Data_and_operation;

public class StringApp {

	public static void main(String[] args) {
		System.out.println("Hello World!!"); // Sring
		//System.out.println('Hello World!!'); // 오류
		System.out.println('A'); // Character
		
		// new line
		System.out.println("Hello \nWorld!!"); // \n : 줄바꿈
		System.out.println("Hello \"World\""); // "" 다중 사용 -> Hello "World"로 출력
	} // main

} // class
